#3. Redondeo de número flotante Enunciado: Crea una variable con un número decimal y redondéalo a 2 decimales.

numero_decimal = 3.14159265358979323846
numero_redondeado = round(numero_decimal, 2)

print(f"El número decimal es: {numero_decimal}, y su tipo es: {type(numero_decimal)}")
print(f"El número redondeado a 2 decimales es: {numero_redondeado}, y su tipo es: {type(numero_redondeado)}")